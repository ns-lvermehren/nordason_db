CREATE OR REPLACE FUNCTION public.fn_calculate_gtin_check_digit(p_gtin12 text)
 RETURNS text
 LANGUAGE plpgsql
AS $function$
DECLARE
    v_sum INT := 0;
    v_digit INT;
    v_check_digit INT;
BEGIN
    -- Gewichtung: 1, 3, 1, 3... von rechts nach links
    FOR i IN 1..12 LOOP
        v_digit := CAST(SUBSTRING(p_gtin12 FROM i FOR 1) AS INT);
        IF i % 2 = 0 THEN
            v_sum := v_sum + (v_digit * 3);
        ELSE
            v_sum := v_sum + v_digit;
        END IF;
    END LOOP;
    
    v_check_digit := (10 - (v_sum % 10)) % 10;
    RETURN p_gtin12 || v_check_digit::text;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.fn_manage_product_ids()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
DECLARE
    v_start_val BIGINT;
    v_new_ref BIGINT;
    v_base_gtin TEXT;
    v_check_digit INT;
BEGIN
    -- 1. Lock gegen Race-Conditions bei Nummernvergabe
    PERFORM pg_advisory_xact_lock(hashtext(NEW.article_type::text));

    -- 2. LOGIK FÜR INTERNAL_REFERENCE (Artikelnummer)
    IF NEW.internal_reference IS NULL THEN
        -- Validierung der Pflichtfelder (korrigiert auf Spalte "name")
        IF NEW.name IS NULL OR NEW.article_type IS NULL OR NEW.sellable IS NULL THEN
            RAISE EXCEPTION 'Neuimport fehlgeschlagen: Name, Typ und Sellable müssen ausgefüllt sein!';
        END IF;

        -- Startwert aus der Tabelle "product_type" (Spalte "type") holen
        SELECT start_value INTO v_start_val 
        FROM product_type 
        WHERE type = NEW.article_type;
        
        IF v_start_val IS NOT NULL THEN
            -- Suche nach der nächsten freien Nummer (Gap-Filling Logik)
            SELECT COALESCE(
                (SELECT v_start_val WHERE NOT EXISTS (
                    SELECT 1 FROM product_master WHERE internal_reference = v_start_val::text
                )),
                (SELECT MIN(CAST(NULLIF(internal_reference, '') AS BIGINT) + 1)
                 FROM product_master
                 WHERE article_type = NEW.article_type
                   AND internal_reference ~ '^[0-9]+$' 
                   AND CAST(internal_reference AS BIGINT) >= v_start_val
                   AND NOT EXISTS (
                     SELECT 1 FROM product_master p2 
                     WHERE p2.internal_reference = (CAST(NULLIF(product_master.internal_reference, '') AS BIGINT) + 1)::text
                   ))
            ) INTO v_new_ref;

            NEW.internal_reference := v_new_ref::text;
        END IF;
    END IF;

    -- 3. LOGIK FÜR GTIN GENERIERUNG
    IF NEW.sellable = true AND NEW.gtin IS NULL THEN
        -- Fix: gtin::TEXT casten, um SUBSTRING nutzen zu können
        SELECT MAX(SUBSTRING(gtin::TEXT FROM 1 FOR 12)::BIGINT) INTO v_base_gtin
        FROM product_master
        WHERE gtin::TEXT ~ '^[0-9]{13}$';

        IF v_base_gtin IS NULL THEN
            v_base_gtin := '426000000001'; 
        ELSE
            v_base_gtin := (v_base_gtin::BIGINT + 1)::TEXT;
        END IF;

        -- Prüfziffer berechnen und GTIN zusammensetzen
        v_check_digit := public.gs1_check_digit(v_base_gtin);
        
        -- Zurück zu BIGINT casten für die Zieltabelle
        NEW.gtin := (v_base_gtin || v_check_digit::TEXT)::BIGINT;
    END IF;

    -- Zeitstempel setzen
    NEW.updated_at := CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.gs1_check_digit(gtin12 text)
 RETURNS integer
 LANGUAGE plpgsql
 IMMUTABLE
AS $function$
DECLARE
  sum INT := 0;
  digit INT;
BEGIN
  -- Sicherheitscheck
  IF gtin12 !~ '^[0-9]{12}$' THEN
    RAISE EXCEPTION 'Ungültige GTIN-12: %', gtin12;
  END IF;

  -- GS1-Prüfzifferberechnung
  FOR i IN 1..12 LOOP
    digit := SUBSTRING(gtin12 FROM (13 - i) FOR 1)::INT;
    IF i % 2 = 1 THEN
      sum := sum + digit * 3;
    ELSE
      sum := sum + digit;
    END IF;
  END LOOP;

  RETURN (10 - (sum % 10)) % 10;
END;
$function$
;

