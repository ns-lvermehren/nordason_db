create or replace view "public"."bom_explosion_view1" as  WITH RECURSIVE clean_bom AS (
         SELECT b.parent,
            b.child,
            b.qty,
            b.brand,
            pm_p.name AS parent_name,
            pm_p.gtin AS parent_gtin,
            pm_c.name AS child_name,
            pm_c.gtin AS child_gtin
           FROM ((public.bom b
             JOIN public.product_master pm_p ON ((pm_p.internal_reference = b.parent)))
             JOIN public.product_master pm_c ON ((pm_c.internal_reference = b.child)))
        ), bom_explosion AS (
         SELECT cb.parent AS root_ref,
            cb.child AS current_child,
            1 AS level,
            cb.qty AS qty_total,
            cb.brand,
            ARRAY[cb.parent, cb.child] AS path_refs,
            ARRAY[cb.parent_name, cb.child_name] AS path_names_pure,
            ARRAY[cb.parent_gtin, cb.child_gtin] AS path_gtins,
            ARRAY[cb.qty] AS path_qtys
           FROM clean_bom cb
          WHERE (NOT (EXISTS ( SELECT 1
                   FROM clean_bom x
                  WHERE (x.child = cb.parent))))
        UNION ALL
         SELECT be_1.root_ref,
            cb.child,
            (be_1.level + 1),
            (be_1.qty_total * cb.qty),
            be_1.brand,
            (be_1.path_refs || cb.child),
            (be_1.path_names_pure || cb.child_name),
            (be_1.path_gtins || cb.child_gtin),
            (be_1.path_qtys || cb.qty)
           FROM (clean_bom cb
             JOIN bom_explosion be_1 ON ((cb.parent = be_1.current_child)))
          WHERE (NOT (cb.child = ANY (be_1.path_refs)))
        )
 SELECT root_ref,
    brand,
    level AS depth,
    qty_total,
    ((path_refs[1] || ' - '::text) || path_names_pure[1]) AS root_info,
    ((path_refs[2] || ' - '::text) || path_names_pure[2]) AS level_1_info,
    path_gtins[2] AS level_1_gtin,
    path_qtys[1] AS level_1_qty,
        CASE
            WHEN ((level = 2) AND (path_names_pure[3] IS NOT NULL) AND (path_names_pure[3] !~~* '%polybag%'::text) AND (path_names_pure[2] !~~* '%polybag%'::text)) THEN ((path_refs[3] || ' - Polybag '::text) || path_names_pure[3])
            ELSE ((path_refs[3] || ' - '::text) || path_names_pure[3])
        END AS level_2_info,
    path_gtins[3] AS level_2_gtin,
    path_qtys[2] AS level_2_qty,
        CASE
            WHEN ((level = 3) AND (path_names_pure[4] IS NOT NULL) AND (path_names_pure[4] !~~* '%polybag%'::text) AND (path_names_pure[3] !~~* '%polybag%'::text)) THEN ((path_refs[4] || ' - Polybag '::text) || path_names_pure[4])
            ELSE ((path_refs[4] || ' - '::text) || path_names_pure[4])
        END AS level_3_info,
    path_gtins[4] AS level_3_gtin,
    path_qtys[3] AS level_3_qty,
        CASE
            WHEN ((level = 4) AND (path_names_pure[5] IS NOT NULL) AND (path_names_pure[5] !~~* '%polybag%'::text) AND (path_names_pure[4] !~~* '%polybag%'::text)) THEN ((path_refs[5] || ' - Polybag '::text) || path_names_pure[5])
            ELSE ((path_refs[5] || ' - '::text) || path_names_pure[5])
        END AS level_4_info,
    path_gtins[5] AS level_4_gtin,
    path_qtys[4] AS level_4_qty
   FROM bom_explosion be
  WHERE (NOT (EXISTS ( SELECT 1
           FROM clean_bom x
          WHERE (x.parent = be.current_child))))
  ORDER BY root_ref, path_refs;


