CREATE OR REPLACE PROCEDURE public.pr_migrate_staging_to_master()
 LANGUAGE plpgsql
AS $procedure$
DECLARE
    rows_affected INT;
BEGIN
    -- Der eigentliche Import-Befehl
    INSERT INTO product_master (
        article_type, 
        product_name, 
        internal_reference, 
        gtin_13, 
        sellable
    )
    SELECT 
        INITCAP(LOWER(COALESCE(s.article_type, p.article_type))),
        COALESCE(s.product_name, p.product_name),
        s.internal_reference, 
        s.gtin_13, 
        COALESCE(s.sellable, p.sellable)
    FROM stg_product_master s
    LEFT JOIN product_master p ON s.internal_reference = p.internal_reference
    WHERE 
        -- Validierung: Bestand existiert ODER Neuimport ist vollständig
        (p.internal_reference IS NOT NULL) OR 
        (s.article_type IS NOT NULL AND s.product_name IS NOT NULL AND s.sellable IS NOT NULL)
    ON CONFLICT (internal_reference) 
    DO UPDATE SET 
        gtin_13 = EXCLUDED.gtin_13,
        sellable = COALESCE(EXCLUDED.sellable, product_master.sellable),
        product_name = COALESCE(EXCLUDED.product_name, product_master.product_name),
        updated_at = CURRENT_TIMESTAMP;

    -- Info ausgeben, wie viele Zeilen verarbeitet wurden
    GET DIAGNOSTICS rows_affected = ROW_COUNT;
    RAISE NOTICE 'Migration abgeschlossen: % Zeilen verarbeitet.', rows_affected;
END;
$procedure$
;

CREATE OR REPLACE FUNCTION public.split_storage_location_name()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
BEGIN
    -- Regex-Erklärung: 
    -- ^           = Start des Textes
    -- [^-]+       = Mindestens ein Zeichen, das kein Bindestrich ist
    -- -           = Ein Bindestrich
    -- {3}         = Das Ganze (Zeichen + Bindestrich) exakt dreimal
    -- [^-]+       = Ein letztes Segment ohne Bindestrich
    -- $           = Ende des Textes
    
    IF NEW.name ~ '^[^-]+-[^-]+-[^-]+-[^-]+$' THEN
        NEW.gang  := split_part(NEW.name, '-', 1);
        NEW.regal := split_part(NEW.name, '-', 2);
        NEW.ebene := split_part(NEW.name, '-', 3);
        NEW.platz := split_part(NEW.name, '-', 4);
    ELSE
        -- Falls das Muster nicht exakt 4 Teile hat:
        NEW.gang  := NULL;
        NEW.regal := NULL;
        NEW.ebene := NULL;
        NEW.platz := NULL;
    END IF;
    
    RETURN NEW;
END;
$function$
;

